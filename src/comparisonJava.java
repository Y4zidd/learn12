public class comparisonJava {
    public static void main(String[] args) {
        //  equal (==)
        int x, y;

        x = 10;
        y = 10;
        System.out.println(x == y);

        // not equal (!=)
        x = 10;
        y = 10;
        System.out.println(x != y);

        // grater than (>)
        x = 10;
        y = 8;
        System.out.println(x > y);

        // less than (<)
        x = 8;
        y = 10;
        System.out.println(x < y);

        // greater than or equal to (>=)
        x = 10;
        y = 10;
        System.out.println(x >= y);

        // less than or equal to (<=)
        x = 11;
        y = 10;
        System.out.println(x <= y);
    }
}