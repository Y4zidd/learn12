public class operatorjava {
    public static void main(String[] args) {
        // oparator +
        int x, y;

        x = 10;
        y = 12;
        System.out.println(x+y);

        // operator -
        x = 12;
        y = 10;
        System.out.println(x-y);

        // operator x
        x = 2;
        y = 10;
        System.out.println(x*y);

        // oparator :
        x = 10;
        y = 2;
        System.out.println(x/y);

        // modulus % (sisa bagi)
        x = 10;
        y = 3;
        System.out.println(x%y);

        // increment ++ (di tambah 1)
        x = 1;
        x++;
        System.out.println(x);

        // decrement -- (di kurang 1)
        x = 2;
        x--;
        System.out.println(x);
    }
}